package tdd;

public class Example {
    public int sum(int a, int b) { //method sum is setting and getting
        return a+b;
    }

    public int sum(int a, int b, int c) { //method overloading (overloading method sum)
        return a+b+c;
    }

    public boolean booleanMethod() { // boolean method to return true
        return true;
    }

    public int sum(int a, int b, int c, int d) {
        return a + b + c+ d;
    }
}
